                String DATE_FActu  = rs.getString("DATE_J_F_ACTU");
                String HEURE_Actu = rs.getString("HEURE_F_ACTU");
                String LINK_Actu = rs.getString("LINK_ACTU");
                String TEXT_Actu  = rs.getString("TEXT_ACTU");
                
                String[] arrOfStr1M = DATE_DActu.split(" ", 2);
                DATE_DActu = arrOfStr1M[0];
                String[] arrOfStr2M = DATE_FActu.split(" ", 2);
                DATE_FActu = arrOfStr2M[0];
                
                descrActuM.setText(TEXT_Actu);
                titreActuM.setText(TITRE_Actu);
                dateD.setText(DATE_DActu);
                dateF.setText(DATE_FActu);
                heurD.setText(HEURE_Actu);
