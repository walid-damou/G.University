                + "DATE_J_D_ACTU = TO_DATE('"+EvDateDeb+"', 'YYYY-MM-DD'), "
                + "DATE_J_F_ACTU = TO_DATE('"+EvDateFin+"', 'YYYY-MM-DD'),"
                + "HEURE_F_ACTU = '"+EvHeurDib+"', LINK_ACTU = '"+Link+"', "
                + "TEXT_ACTU = '"+EvDescreption+"' WHERE ID_ACTU = "+ID;
